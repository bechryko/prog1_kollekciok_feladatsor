public class Jatek {
    private String nev;
    private int prioritas;

    public Jatek(String nev, int prioritas) {
        this.nev = nev;
        this.prioritas = prioritas;
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public int getPrioritas() {
        return prioritas;
    }

    public void setPrioritas(int prioritas) {
        this.prioritas = prioritas;
    }
}
